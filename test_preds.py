def getPredictions(tlist):
    s3 = boto3.resource('s3')
    model = pickle.loads(s3.Bucket("s3tmc101").Object("pickle_model.pkl").get()['Body'].read())
    t_df = pd.DataFrame(tlist)
    predictions = model.predict(t_df)
    return predictions


